                return false;
            }
            return (float) $score >= (float) $class->success_grade;
        })->values();

        if ($passingTrainees->isEmpty()) {
            return response()->json([
                'message' => 'No trainees found with exam_score greater than or equal to success_grade.',
                'generated_count' => 0,
                'skipped_count' => $trainees->count(),
                'details' => [],
            ], 201);
