                ->first();

            if ($existingCertificate) {
                $skipped++;
                $details[] = [
                    'trainee_id' => $trainee->id,
                    'name' => $fullName,
                    'reason' => 'existing_certificate',
                    'certificate_id' => $existingCertificate->id,
                ];
